class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int ans=N;
        int count=0;
        while(N>0){
            int d=N%10;
            if(d==0){
                N/=10;
                continue;
            }
            else if(ans%d==0){
                count++;
            }

            N/=10;
        }
        return count;
    }
};

